create table ww_word_pairs (
  id bigint generated by default as identity primary key,
  villager text not null,
  wolf text not null,
  category text not null,
  constraint ww_word_pairs_unique unique (villager, wolf, category)
);

create table ww_rooms (
  code text primary key,
  host_id uuid not null,
  phase text not null default 'lobby' check (phase in ('lobby', 'discussion', 'result')),
  round integer not null default 0,
  settings jsonb not null default '{"discussionSeconds": 180, "wolfCount": 1}'::jsonb,
  players jsonb not null default '[]'::jsonb,
  current_game jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

