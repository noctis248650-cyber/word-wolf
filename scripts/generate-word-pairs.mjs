const rows = [];
const seen = new Set();

function addPair(villager, wolf, category) {
  if (!villager || !wolf || !category || villager === wolf) return;
  const key = `${villager}\t${wolf}\t${category}`;
  const reverseKey = `${wolf}\t${villager}\t${category}`;
  if (seen.has(key) || seen.has(reverseKey)) return;
  seen.add(key);
  rows.push([villager, wolf, category]);
}

for (const [category, clusters] of categories) {
  for (const words of clusters) {
    for (let left = 0; left < words.length - 1; left += 1) {
      for (let right = left + 1; right < words.length; right += 1) {
        addPair(words[left], words[right], category);
      }
    }
  }
}

if (categories.length !== 50) {
  throw new Error(`Expected 50 categories, got ${categories.length}.`);
}

if (rows.length !== 1000) {
  throw new Error(`Expected 1000 word pairs, got ${rows.length}.`);
}

writeFileSync(outputPath, ["villager,wolf,category", ...rows.map((row) => row.join(",")), ""].join("\n"), "utf8");
console.log(`Generated ${rows.length} curated word pairs across ${categories.length} categories.`);
